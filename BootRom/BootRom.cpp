BootRom::BootRom(std::shared_ptr<Bus> bus, std::shared_ptr<PPU> ppu)
	:mBus(bus), mPPU(ppu)
{
}

void BootRom::initializeBootRom()
{
	extractBootLogo();
	mScrollingIter = 0;
	mBus->write(SCY, 0x64);
}

void BootRom::execute()
{
	if (!mIsBootFinished)
	{
		scrollLogo();
	}
	else
		GBE_LOG_INFO("PLAY SOUND");
}

void BootRom::scrollLogo()
{
	if (mScrollingIter < mTotalScrollingIter)
	{
		if (mVBLankCnt == 0)
			mVBLankCnt++;
		else
		{
			mPPU->decSCY();
			mScrollingIter++;
			mVBLankCnt = 0;
		}
	}
	else
	{
		mIsBootFinished = true;
	}
}



